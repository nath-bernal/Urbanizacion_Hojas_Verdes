    private void buscarButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarButton1ActionPerformed
        Gson gson = new Gson();
        List<Propietario> propietarios = new ArrayList<>();

        File archivoPropietarios = new File("propietarios.json");
        if (archivoPropietarios.exists() && archivoPropietarios.length() != 0) {
            try (FileReader reader = new FileReader(archivoPropietarios)) {
                java.lang.reflect.Type propietarioListType = new TypeToken<ArrayList<Propietario>>(){}.getType();
                propietarios = gson.fromJson(reader, propietarioListType);
            } catch (IOException e) {
                e.printStackTrace();
            }
        }

        Propietario propietarioEncontrado = null;
        for (Propietario propietario : propietarios) {
            if (propietario.getCedula().equals(buscarCedulaCampo.getText().trim())) {
                propietarioEncontrado = propietario;
                break;
            }
        }

        if (propietarioEncontrado != null) {
            nombreCampo.setText(propietarioEncontrado.getNombre());
            numeroPropiedadCampo.setText(propietarioEncontrado.getNumeroPropiedad());
            correoCampo.setText(propietarioEncontrado.getCorreo());

            JOptionPane.showMessageDialog(this, "Propietario encontrado, puede actualizar o eliminar su información");

        } else {
            JOptionPane.showMessageDialog(this, "Propietario no encontrado");
        }
    }//GEN-LAST:event_buscarButton1ActionPerformed

