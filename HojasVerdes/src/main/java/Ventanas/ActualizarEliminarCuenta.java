    private void actualizarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarButtonActionPerformed

        String nuevoNombre = nombreCampo.getText().trim();
        String nuevoNumeroPropiedad = numeroPropiedadCampo.getText().trim();
        String nuevoCorreo = correoCampo.getText().trim();
        Gson gson = new Gson();
        List<Propietario> propietarios = new ArrayList<>();

        File archivoPropietarios = new File("propietarios.json");
        if (archivoPropietarios.exists() && archivoPropietarios.length() != 0) {
            try (FileReader reader = new FileReader(archivoPropietarios)) {
                java.lang.reflect.Type propietarioListType = new TypeToken<ArrayList<Propietario>>(){}.getType();
                propietarios = gson.fromJson(reader, propietarioListType);
            } catch (IOException e) {
                e.printStackTrace();
            }
        }

        for (Propietario propietario : propietarios) {
            if (propietario.getCedula().equals(buscarCedulaCampo.getText().trim())) {
                propietario.setNombre(nuevoNombre);
                propietario.setNumeroPropiedad(nuevoNumeroPropiedad);
                propietario.setCorreo(nuevoCorreo);
                break;
            }
        }

        try (FileWriter writer = new FileWriter("propietarios.json")) {
            gson.toJson(propietarios, writer);
        } catch (IOException e) {
            e.printStackTrace();
        }

        JOptionPane.showMessageDialog(this, "Información actualizada exitosamente");

      
    }//GEN-LAST:event_actualizarButtonActionPerformed

    private void eliminarCuentaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarCuentaButtonActionPerformed
        Gson gson = new Gson();
        List<Propietario> propietarios = new ArrayList<>();

        File archivoPropietarios = new File("propietarios.json");
        if (archivoPropietarios.exists() && archivoPropietarios.length() != 0) {
            try (FileReader reader = new FileReader(archivoPropietarios)) {
                java.lang.reflect.Type propietarioListType = new TypeToken<ArrayList<Propietario>>(){}.getType();
                propietarios = gson.fromJson(reader, propietarioListType);
            } catch (IOException e) {
                e.printStackTrace();
            }
        }

        Propietario propietarioAEliminar = null;
        for (Propietario propietario : propietarios) {
            if (propietario.getCedula().equals(buscarCedulaCampo.getText().trim())) {
                propietarioAEliminar = propietario;
                break;
            }
        }

        if (propietarioAEliminar != null) {
            propietarios.remove(propietarioAEliminar);

            // Guardar la lista actualizada en el archivo JSON de propietarios
            try (FileWriter writer = new FileWriter("propietarios.json")) {
                gson.toJson(propietarios, writer);
            } catch (IOException e) {
                e.printStackTrace();
            }

            JOptionPane.showMessageDialog(this, "Cuenta eliminada exitosamente");
        } else {
            JOptionPane.showMessageDialog(this, "Propietario no encontrado");
        }
    }//GEN-LAST:event_eliminarCuentaButtonActionPerformed

